                  <h1>Selected IMI projects and their datasets</h1>
                  <p>As there are more than 100 IMI/IMI2 projects and even more data sources from pharmaceutical industry, an important task is to identify data sets representing the main data types for initial FAIRification.</p>

                  <p>Based on a range of criteria, such as scientific value, societal impact, or relevance to the scientific community, FAIRplus selects datasets from a set of IMI projects which are prioritised for FAIRification.</p>

                  <p>In 2019, datasets from four IMI projects were selected for early FAIRification. The FAIRified data from these four projects are now available in the <a href="http://datacatalog.elixir-luxembourg.org/">IMI FAIR Data Catalogue.</a> </p>

                  <p>A further nine IMI datasets have been identified in 2020, with the potential to generate high societal impact upon FAIRification, an important goal of FAIRplus.</p>

                  <p>The work of the consortia which generated the datasets covers a diverse range of data types and scientific domains, from environmental toxicity assessment methods through to identification of new targets relevant to neurodegenerative diseases. </p>

                    <p>IMI projects selected in 2020:</p>

                    <h2 class="x-top-margin">UltraDD: Unrestricted leveraging of targets for research advancement and drug discovery</h2>
                    <p>The ULTRA-DD project’s goal is to deliver new tools and resources to speed up the development of truly innovative medicines, especially in the areas of autoimmune and inflammatory diseases, where new treatments are urgently needed. Through the Structural Genomics Consortium, ULTRA-DD has strong ties with similar initiatives elsewhere in the world, and this, coupled with the project’s strong open access policy, will ensure that the tools, resources, and knowledge generated by the project will benefit the entire scientific community and hopefully result in new drug discovery programmes.</p>
                    <ul>
                      <li>
                    <a href="https://www.ultra-dd.org">Project website</a>
                      </li>
                    </ul>

                    <h2 class="x-top-margin">UBIOPRED: Unbiased biomarkers for the prediction of respiratory disease outcomes</h2>
                    <p>U-BIOPRED project made a groundbreaking step forward in understanding severe asthma by uncovering a number of subtypes of this disease. The discovery is already helping researchers from universities, pharmaceutical companies and patient groups in their search for new, more effective treatments.</p>
                    <ul>
                      <li>
                    <a href="https://www.europeanlung.org/en/projects-and-research/projects/u-biopred/home">Project website</a>
                      </li>
                    </ul>

                      
                    <h2 class="x-top-margin">IMIDIA: Improving beta-cell function and identification of diagnostic biomarkers For treatment monitoring in diabetes</h2>
                    <p>The IMIDIA project sought to address the key bottlenecks in the development of beta cell focused therapies, to accelerate the path to improved diabetes management and ultimately pave the way to curing the disease. The project brought together about 100 researchers who were able to develop a human pancreatic beta-cell line that could be studied in the lab, removing one of the major bottlenecks in diabetes research. </p>
</p>
                    <ul>
                      <li>
                    <a href="http://www.imidia.org/">Project website</a>
                      </li>
                    </ul>

                    
                    <h2 class="x-top-margin">RHAPSODY: Assessing risk and progression of prediabetes and type 2 diabetes to enable disease modification</h2>
                    <p>RHAPSODY brings together experts from universities, large pharmaceutical companies and biotechs. Their goal is to add to our understanding of the factors that drive the progression of pre-diabetes to diabetes, and the deterioration of the condition of people with diabetes. RHAPSODY aims to develop novel biological markers that will aid in the diagnosis of type 2 diabetes, and the identification of different sub-groups of patients. This information will also help to inform clinical trial design and the development of new strategies to prevent and treat diabetes.</p>
                    <ul>
                      <li>
                    <a href="https://imi-rhapsody.eu/">Project website</a>
                      </li>
                    </ul>
                    
                    <h2 class="x-top-margin">EQIPD: European Quality In Preclinical Data</h2>
                    <p>The goal of the EQIPD project is to deliver simple recommendations to facilitate data quality without impacting innovation. The project team works towards defining components of an EQIPD quality management system achieving consensus among different stakeholders on quality management recommendations for research. The project will also set up an online educational platform that will deliver certified courses in the principles and application of data quality and rigour.</p>
                    <ul>
                      <li>
                    <a href="http://eqipd.org/">Project website</a>
                      </li>
                    </ul>
                    
                    <h2 class="x-top-margin">ABIRISK: Anti-Biopharmaceutical Immunization: Prediction and Analysis of Clinical Relevance to Minimize the Risk</h2>
                    <p>The aim of the ABIRISK project was to shed new light on the factors behind immunogenicity and get a better understanding of its molecular origin. The result is an improvement in tests designed to predict the likeliehood that a molecule will trigger an immune response.</p>
                    <ul>
                      <li>
                    <a href="http://www.abirisk.eu/">Project website</a>
                      </li>
                    </ul>
                    
                    <h2 class="x-top-margin">EBISC I:European Bank for induced pluripotent Stem Cells</h2>
                    <p>EBiSC was established to provide researchers across academia and the pharmaceutical industry with disease-relevant, quality-controlled, research-grade Induced pluripotent stem cells (iPSC) lines, data and cell services, and to promote the wider use of iPSCs with the ultimate goal of improving health. There are more than 800 iPSC lines in the EBiSC catalogue, covering neurodegenerative diseases, diabetes, eye and heart diseases, muscular dystrophies, and lines from healthy control donors.</p>
                    <ul>
                      <li>
                    <a href="https://www.ebisc.org/">Project website</a>
                      </li>
                    </ul>
                    
                    <h2 class="x-top-margin">EBiSC2: A sustainable European bank for induced pluripotent stem cells</h2>
                    <p>EBiSC2 aims to become a self-sustaining biobank, providing well-characterised, quality controlled Induced pluripotent stem cells (iPSC) to the academic and industrial scientific communities worldwide. Building on the work of previous projects, including IMI’s EBiSC, EBiSC2 will provide access to a broad catalogue of iPSCs from diverse disease and genetic backgrounds along with comprehensive datasets. They will also offer the bulk production of iPSCs and the delivery of pre-differentiated cell populations. By supporting future iPSC-based disease research, EBiSC2 will contribute to the discovery and development of new drugs.</p>
                    <ul>
                      <li>
                    <a href="https://ebisc.org">Project website</a>
                      </li>
                    </ul>
                    
                    <h2 class="x-top-margin">iPIE: Intelligent Assessment of Pharmaceuticals in the Environment</h2>
                    <p>iPiE project’s goal is to develop a framework for methodologies to prioritise new and existing medicinal compounds for a comprehensive environmental risk assessment. As such it supports and inform regulatory activities designed to assess and reduce the environmental impact of medicines.</p>
                    <ul>
                      <li>
                    <a href="http://i-pie.org/">Project website</a>
                      </li>
                    </ul>
                    

                

