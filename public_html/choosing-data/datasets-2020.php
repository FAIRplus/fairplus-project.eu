                  <h1>Datasets 2020</h1>

                  <p>In 2020, <strong>datasets from nine IMI projects</strong> were selected for FAIRification. These have potential to generate high societal impact upon FAIRification, an important goal of FAIRplus.</p>
                  <ul>
                    <li><a href="#ultradd">Ultra-DD</a><br />(Unrestricted Leveraging of Targets for Research Advancement and Drug Discovery)</li>
                    <li><a href="#ubiopred">UBIOPRED</a><br />(Unbiased Biomarkers for the Prediction of Respiratory Disease Outcomes)</li>
                    <li><a href="#imidia">IMIDIA</a><br />(Improving Beta-cell Function and Identification of Diagnostic Biomarkers for Treatment Monitoring in Diabetes)</li>
                    <li><a href="#rhapsody">RHAPSODY</a><br />(Assessing risk and progression of prediabetes and type 2 diabetes to enable disease modification)</li>
                    <li><a href="#equipd">EQIPD</a><br />(European Quality in Preclinical Data)</li>
                    <li><a href="#abirisk">ABIRISK</a><br />(Anti-Biopharmaceutical Immunization: Prediction and Analysis of Clinical Relevance to Minimize the Risk)</li>
                    <li><a href="#ebisc">EBISC</a><br />(European Bank for induced pluripotent Stem Cells)</li>
                    <li><a href="#ebisc2">EBiSC2</a><br />(A sustainable European Bank for induced pluripotent Stem Cells)</li>
                    <li><a href="#ipie">iPIE</a><br />(Intelligent Assessment of Pharmaceuticals in the Environment)</li>
                  </ul>

                  <p>The projects cover a diverse range of data types and scientific domains, from environmental toxicity assessment methods through to identification of new targets relevant to neurodegenerative diseases. </p>

                    <p>IMI projects selected in 2020:</p>

                    <h2 class="x-top-margin" id="ultradd">Ultra-DD</h2>
                    <p>The <a href="https://www.ultra-dd.org">ULTRA-DD project</a>'s goal is to deliver new tools and resources to speed up the development of truly innovative medicines, especially in the areas of autoimmune and inflammatory diseases, where new treatments are urgently needed. </p>
                    <p>Through the Structural Genomics Consortium, ULTRA-DD has strong ties with similar initiatives elsewhere in the world, and this, coupled with the project's strong open access policy, will ensure that the tools, resources, and knowledge generated by the project will benefit the entire scientific community and hopefully result in new drug discovery programmes.</p>

                    <h2 class="x-top-margin" id="ubiopred">UBIOPRED</h2>
                    <p>The <a href="https://www.europeanlung.org/en/projects-and-research/projects/u-biopred/home">U-BIOPRED project</a> made a groundbreaking step forward in understanding severe asthma by uncovering a number of subtypes of this disease. The discovery is already helping researchers from universities, pharmaceutical companies and patient groups in their search for new, more effective treatments.</p>

                    <h2 class="x-top-margin" id="imidia">IMIDIA</h2>
                    <p>The <a href="http://www.imidia.org/">IMIDIA project</a> sought to address the key bottlenecks in the development of beta cell focused therapies, to accelerate the path to improved diabetes management and ultimately pave the way to curing the disease. The project brought together about 100 researchers who were able to develop a human pancreatic beta-cell line that could be studied in the lab, removing one of the major bottlenecks in diabetes research. </p>

                    <h2 class="x-top-margin" id="rhapsody">RHAPSODY</h2>
                    <p><a href="https://imi-rhapsody.eu/">RHAPSODY</a> brings together experts from universities, large pharmaceutical companies and biotechs. Their goal is to add to our understanding of the factors that drive the progression of pre-diabetes to diabetes, and the deterioration of the condition of people with diabetes. RHAPSODY aims to develop novel biological markers that will aid in the diagnosis of type 2 diabetes, and the identification of different sub-groups of patients. This information will also help to inform clinical trial design and the development of new strategies to prevent and treat diabetes.</p>

                    <h2 class="x-top-margin" id="eqipd">EQIPD</h2>
                    <p>The goal of the <a href="http://eqipd.org/">EQIPD project</a> is to deliver simple recommendations to facilitate data quality without impacting innovation. The project team works towards defining components of an EQIPD quality management system achieving consensus among different stakeholders on quality management recommendations for research. The project will also set up an online educational platform that will deliver certified courses in the principles and application of data quality and rigour.</p>

                    <h2 class="x-top-margin" id="abirisk">ABIRISK</h2>
                    <p>The aim of the <a href="http://www.abirisk.eu/">ABIRISK project</a> was to shed new light on the factors behind immunogenicity and get a better understanding of its molecular origin. The result is an improvement in tests designed to predict the likeliehood that a molecule will trigger an immune response.</p>

                    <h2 class="x-top-margin" id="ebisc">EBISC I</h2>
                    <p><a href="https://www.ebisc.org/">EBiSC</a> was established to provide researchers across academia and the pharmaceutical industry with disease-relevant, quality-controlled, research-grade Induced pluripotent stem cells (iPSC) lines, data and cell services, and to promote the wider use of iPSCs with the ultimate goal of improving health. There are more than 800 iPSC lines in the EBiSC catalogue, covering neurodegenerative diseases, diabetes, eye and heart diseases, muscular dystrophies, and lines from healthy control donors.</p>


                    <h2 class="x-top-margin" id="ebisc2">EBiSC2</h2>
                    <p><a href="https://ebisc.org">EBiSC2</a> aims to become a self-sustaining biobank, providing well-characterised, quality controlled Induced pluripotent stem cells (iPSC) to the academic and industrial scientific communities worldwide. Building on the work of previous projects, including IMIâs EBiSC, EBiSC2 will provide access to a broad catalogue of iPSCs from diverse disease and genetic backgrounds along with comprehensive datasets. They will also offer the bulk production of iPSCs and the delivery of pre-differentiated cell populations. By supporting future iPSC-based disease research, EBiSC2 will contribute to the discovery and development of new drugs.</p>


                    <h2 class="x-top-margin" id="ipie">iPIE</h2>
                    <p>The goal of the <a href="http://i-pie.org/">iPiE project</a> is to develop a framework for methodologies to prioritise new and existing medicinal compounds for a comprehensive environmental risk assessment. As such it supports and informs regulatory activities designed to assess and reduce the environmental impact of medicines.</p>




