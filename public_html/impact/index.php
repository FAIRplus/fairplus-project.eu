                  <p>One goal of FAIRplus is to ensure that policies and funders support the FAIRification of data, and that they support the long-term storage of FAIRified data. </p>
                  <p>To this end, we engage with policy makers at the national and European level. These policy makers include publishers, the OECD, the European Commission and the Open Science Policy Platform. We attend relevant events, engage formally at specific meetings, and prepare position papers and consultation responses.</p>
                  <p>This dialogue is supported by published materials from the project, such as FAIR
metrics and the scientific use cases demonstrating the benefits of FAIRification. The materials will also cover how to ensure the long-term funding and sustainability of the FAIR databases, as well as policies covering data protection and the secure access to data. For example, the FAIR metrics and FAIRification resources will inform journals how to enhance their data policies for authors, to ensure that data will be increasingly FAIR.</p>

<h2>KPI Dashboard</h2>
