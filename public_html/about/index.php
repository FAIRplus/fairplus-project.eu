                   <p>The volume and complexity of life science data being produced by research is growing exponentially. To gain most benefit from this data, it needs to be available to researchers. Unfortunately it is often inaccessible, annotated inconsistently, and difficult to share because it is in proprietary formats.</p>
                   <p>The FAIRplus project aims to address these issues by developing guidelines and tools to make data FAIR:</p>
                   <ul class="line-height-1">
                       <li><strong>F</strong>indable</li>
                       <li><strong>A</strong>ccessible</li>
                       <li><strong>I</strong>nteroperable</li>
                       <li><strong>R</strong>eusable</li>
                   </ul>
                   <p>It aims to increase the discovery, accessibility and reusability of data from selected <a href="https://www.imi.europa.eu/">Innovative Medicine Initiative (IMI)</a> projects, as well as internal data from pharmaceutical industry partners. It will also organise training for data scientists in academia, SMEs and pharmaceutical companies to enable wider adoption of best practises in life science data management.</p>

                   <p>The increased FAIRness of data will lead to a wider sharing of knowledge, greater opportunities for innovation, and more insights that benefit society. See our <a href="https://fairplus-project.eu/impact/kpi-dashboard">KPI Dashboard</a> to track our progress in FAIRifying datasets from IMI projects, and disseminating best practices in FAIR data management.</p>


                   <p>See <a href="how-project-organised">How the project is organised</a> for a more detailed overview.</p>

<h2 class="x-top-margin">Frequently Asked Questions about FAIR data and FAIRplus</h2>



<h3 id="why-fair">1. Why make data FAIR?</h3>
<p>Data is more easily found, accessed and reused when its metadata is harmonised and its interoperability is increased. The aim of FAIRifying is to enhance data's value by adding identifiers, metadata and links to ontologies. This will also enhance the data's sustainability.</p>

<h3 id="benefits">2. What are the immediate visible benefits?</h3>
<p><a href="https://fairplus-project.eu/resources/">Following the guidelines we are preparing</a>, we hope that datasets can be more easily FAIRified by referring to previous approaches that have been successfully applied. These established methodologies should advance the dataset to a higher maturity level as assessed by the <a href="https://drive.google.com/open?id=16QaBaoGXLJnJtZTjWlzWvw_Pkwneu8Z9" target="_blank">Capability Maturity Model Integration Framework (CMMI)</a>. As an immediate result of this process, public and private datasets will be easier to locate, access and analyse. Interoperability should be enhanced, which means the data can be combined with other FAIRified data (public and proprietary) for more comprehensive research results.</p>

<h3 id="requirements">3. What are the minimum data requirements to be able to start the FAIRification processes?</h3>
<p>There is no simple answer to this question, but one key condition for FAIRfication is having the data in a digitised format. Data recorded on paper only or in other non-digitised formats cannot readily be FAIRified. Analogue records that can be digitised are, however, in focus. Data owners have to be certain about the data provenance and have a clear view of the reasons for FAIRification, as the entire process is not yet automated. A legal framework for accessing the data is also needed.</p>

<h3 id="focus">4. Which data do you have in focus?</h3>
<p>The FAIRplus consortium is supported by the Innovative Medicines Initiative (IMI), a public private partnership with the European pharmaceutical industry. Academic institutions  work together with pharma partners to make their data FAIR.</p>
<p>In the context of FAIRification of data, pharma datasets, especially in research and development, are not so different from the academic ones. The focus is on pharmaceutically relevant datasets, ranging from basic research to preclinical and clinically relevant datasets. The same FAIRification processes will be applied to the data <a href="https://fairplus-project.eu/choosing-data/">from selected IMI projects</a>. This will ensure the interoperability of these data with other archives in the public and private sectors.</p>


