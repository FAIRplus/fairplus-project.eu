                  <h1>First results presented at the FAIRplus  Innovation and SME Forum</h1>
<p>Nearly one hundred scientists, data managers and IT experts from academia, Small-Medium Enterprises (SMEs) and industry gathered in Hinxton, UK, to discuss the challenges and opportunities in implementing FAIR data principles in life science research.</p>

<p>The first FAIRplus Innovation and SME Forum held on 29 January 2020 presented the practical resources necessary for successful implementation of FAIR data principles. The participants also explored the different ways FAIR data can be implemented in pharmaceutical companies, SMEs and academic teams.</p>

