<h2>First results in FAIRplus</h2>

<p>While the principles of data in life sciences have been growing in popularity within the research community, relatively little attention has been given to the actual technical, scientific as well as cultural implications of making biological data FAIR. <p>

</p>The FAIRplus project aims to develop practical resources to enable FAIRification of data; the first half of the programme presented the key outcomes foreseen in the project:
</p>

<ul>
<li><a href="http://fairplus.github.io/the-fair-cookbook">The FAIR cookbook</a>, an open, comprehensive resource with &lsquo;recipes&rsquo; for making different types of life science data FAIR, presented by Philippe Rocca-Serra from the University of Oxford&rsquo;s e-Research Centre.</li>
<li>the FAIR Capability and Maturity Model, a set of guidelines to find the optimum level of FAIR data implementation depending on the needs and resources in each organisation, presented by Oya Beyan from Fraunhofer FIT.</li>
<li>The <a href="https://drive.google.com/file/d/1P2MJSLIST6AJyhmFdIKRfb_Xaqvb9TXX/view">FAIRplus FAIRification process</a> and the first <a href="https://datacatalog.elixir-luxembourg.org/?query=&amp;sort_by=dataset_created&amp;order=desc&amp;groups=IMI%20Projects&amp;page=2">four FAIRified datasets</a> from selected Innovative Medicines Initiative (IMI) projects, presented by Tony Burdett from EMBL-EBI.</li>
<li>The <a href="https://fairplus-project.eu/get-involved/#fellowships">FAIRplus Fellowship Programme</a>, comprehensive FAIR data training to EFPIA members, SMEs and academics, presented by Andreas Pippow from Fraunhofer FIT.</li>
</ul>

<p>
  All four presentations focused on hands-on approaches, highlighting the numerous technical and scientific decisions to be taken when adopting and implementing FAIR data principles.</p>
  <p>
  Jane Lomax, from <a href="https://www.scibite.com/news/a-day-with-the-fairplus-project-implementing-fair-data-principles-in-industrial-life-science-research/">Cambridge-based SME, SciBite said</a>: "Overall the FAIRplus Forum was a fantastic day for us to attend. There are many points at which SciBite can fit into the FAIRification process. We believe that the combination of ontologies, deep learning and FAIR data provide an unparalleled technology set that can directly impact challenges in pharma, agri-science and consumer goods."

</p>
<h2>Value and sustainability of FAIR data</h2>
</p><p>
<p>In the second half of the event discussion turned to the differing approaches to FAIR data in different contexts and organisations.
The Flash Talk session first provided an overview of how FAIR data can bring value to businesses. The session featured seven SMEs who presented how they use FAIR data and what role FAIR data have in their companies. Many of the presentations highlighted the importance of scaling up the FAIRification effort to make it sustainable in the long term. </p><p>
Flash talk presenter, Ted Slater from Elsevier, said: "The FAIRplus Innovation and SME Forum was an outstanding event because of the high concentration of FAIR data experts present. It's rare to have access to that much hands-on experience and deep understanding with FAIR in one place, and I came away with lots of actionable information and insight. I canât wait for the next Forum."</p><p>
Two panel discussions enabled participants to engage in active discussions. The first one explored what FAIR data means in different settings. The panellists &mdash; representatives from organisations offering or using FAIR data services  &mdash; also warned against short-term calculations. In their experience, the impact of FAIR data in the short term is limited, compared to the long-term value resulting from systematic implementation of FAIR principles.</p><p>
The second panel brought together people from different initiatives in FAIR data (FAIRsFAIR, GoFAIR, FAIR4Health, Pistoia Alliance and FAIRplus) to discuss their priorities and approaches to supporting the adoption of FAIR data in industry and academia.
</p>

<figure class="top-margin bottom-margin">
                        <img src="https://fairplus-project.eu/images/fp-02.jpg" alt="Panel discussion" style="max-width: 75%;" class="low-top-margin right-margin">
                    <figcaption>The second panel discussion (from left to right): Erik Schultes, Hugh Shanahan, Helen Parkinson and Venkataraman Shanmugasundaram.</figcaption>
</figure>
<p>The event concluded with a keynote presentation from Carole Goble from the University of Manchester, one of the authors of <a href="https://www.nature.com/articles/sdata201618">the seminal paper that introduced the concept of FAIR data</a>. In her talk &mdash; FAIR History and the Future &mdash; Goble explained the initial intentions of the creators of the FAIR data principles and discussed the challenges in adopting FAIR data at different levels: at the individual level, at the level of a research team, research community and society at large.</p>

