<p>One key topic ran through both panels and appeared in many presentations: the importance of a cultural shift. Serena Scollen, the FAIRplus Project Coordinator, summarised the discussions throughout the day:</p>
<p class="bottom-margin">&ldquo;There are many technical or regulatory aspects we need to take into account and it&rsquo;s great we could discuss them today with our colleagues and partners. But the key to wide adoption of FAIR data principles is our research culture. In ten year&rsquo;s time, I&rsquo;d like to see the FAIR principles ingrained in the way scientists work with and manage data. We should be talking less about how and why, and instead just routinely doing it. I believe the activities and results in FAIRplus help us to get closer to that point!&rdquo;</p>

