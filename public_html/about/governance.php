                  <h2>General Assembly</h2>
                  <p>This is composed of representatives from all participants in the project. The General Assembly has the ultimate decision-making responsibility in matters affecting the overall project strategy, budget and composition of the consortium. It meets once a year.</p>
                  <h2>Managing Board</h2>
                  <p>This is composed of the Work Package Leaders (WPLs), with representives of public and EFPIA members (see <a href="how-project-organised">How the project is organised</a> for an overview of the Work Packages and their leaders). The Board is co-chaired by the Project Coordinator (public) and the Project Leader (EFPIA). It meets monthly (via teleconference) and is responsible for ensuring alignment and coordination across WPs, and for the successful execution of the project. In addition to the monthly teleconferences, the Board meets twice a year for a F2F (once co-located with the General Assembly).</p>
                  <h2>Scientific and Industrial Advisory Board</h2>
                  <p>This is a consultative body of leading global experts in clinical data management, translational medicine, data governance, data archiving, semantics, science policy and ethics from academia, SMEs and EFPIA. The Advisory Board will provide advice to the General Assembly and Managing Board, considering strategy and progress of the project. Their meetings will be held bi-annually and be co-located with the Managing Board F2F meetings.</p>
                  <h2>Ethics Board</h2>
                  <p>The Ethics Board is responsible for the validation of the methodology defined by WP1 to deal with personal and clinical data. It will include explicit confirmation that the beneficiary has or will have a lawful basis for the data processing and that the appropriate technical and organisational measures will be in place to safeguard the rights of the data subjects, also through data anonymisation when applicable. </p>
                  <p>In addition, the Ethics Board will be consulted if and when the need to process personal data arises, their recommendations will be fully implemented and reported in the periodic technical reports. The board will be composed of three external, independent experts with the following backgrounds: lawyer with expertise in data protection and personal rights, lawyer or ethicist with expertise in IT, ethicist.</p>
                  <h2>Project Management</h2>
                  <p>Project Management is shared between the ELIXIR project management unit (public consortium) and Janssen (EFPIA participants). The team will support and report on the execution of the project work plan and budget utilisation, and will provide the mechanism to identify and manage project risks and opportunities. </p>
                  <p>A consortium communication strategy will be established (WP5) making appropriate use of digital resources. Communication dynamics will be promoted across the project, keeping the right level of engagement among stakeholders. Specific collaboration tools will be enabled from the first stages of the project. These tools will guarantee the means for efficient communication within the consortium fulfilling internal communication needs, and external stakeholders needs.</p>
