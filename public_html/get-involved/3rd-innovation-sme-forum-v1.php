                  <p class="xx-top-margin">As part of the FAIRplus project, tools and guidelines have been developed to make life science data FAIR (Findable, Accessible, Interoperable, Reusable), aiming to change the culture of data management in academia, SMEs and pharmaceutical companies.
                  </p>

                  <p>The third FAIRplus Innovation and SME Forum will bring together scientists and data experts from industry and academia interested in FAIRification of life science data. The goal of the event is to present the project's outcome and foster the development of an innovation ecosystem based on the FAIR principles.</p>

                    <h3 class="top-margin">During the event, we will:</h3>
                    <ul>
                        <li>Introduce the FAIR Cookbook, an extensive online resource providing specific tools and procedures (“recipes”) to make and keep data Findable, Accessible, Interoperable and Reusable (FAIR);</li>
                        <li>Share our experiences of FAIRifiying datasets from large European public/private (Innovative Health Initiative; IHI) projects;</li>
                        <li>Show FAIRification in action in an industry context;</li>
                        <li>Discuss the current practises and cost-benefits of FAIR implementation with academics, entrepreneurs and policymakers;</li>
                        <li>Explore strategic partnerships built during the FAIRplus project, such as the collaboration with the Pistoia Alliance.</li>
                    </ul>
