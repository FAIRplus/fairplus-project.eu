                  <h2>The Programme structure</h2>

                  
                  
                  <p>The programme is designed on the basis of the FAIRplus FAIRification Process and consists of a number of compulsory modules corresponding to the steps in the FAIRplus FAIRification process. In addition, there will be several elective modules open to all fellows interested in the content.  

                    <figure class="x-top-margin">
                      <img src="../images/FAIRification-process.png" alt="FAIRification process" class="fill" width="650px"/>
                      <figcaption>FAIRification process developed by the FAIRplus Squad teams.</figcaption>
                    </figure>

                  <p class="x-top-margin">The modules of the FAIRplus FAIRification Process will be grouped into four phases:</p>
                    

                  <p><strong>The first phase</strong> consists of three compulsory modules and three elective modules. Each of these modules is delivered online and should be completed within eight weeks after the programme starts (May 2021). </p>

                     <p><strong>The second phase</strong> is a two-day workshop, which all fellows are expected to attend. During the workshop, participants will work in small groups and will have the opportunity to consult their project with FAIR data experts (members of the FAIRplus Squad teams, FAIR data managers and others).</p>

                    
                     <figure class="float-right percent-40">
                       <img src="../images/t-burdett-speaker.jpg" alt="Tonny Burdett (EMBL-EBI), the FAIRplus Squad teams coordinator, presenting the FAIRification process at the FAIRplus Innovation and SME Forum" class="fill" />
                       <figcaption>Tony Burdett (EMBL-EBI), the FAIRplus Squad teams coordinator, presenting the FAIRification process at the FAIRplus Innovation and SME Forum.</figcaption>
                     </figure>
                     
                     
                    <p><strong>In the third phase</strong>, the fellows will work in pairs to explore interoperability and hosting requirements, and data sharing solutions in different settings. In each pair, the fellows will first assess the interoperability and hosting requirements in their own organisations and then individually arrange visits to their partner's organisations to learn about their specific situations. The collaboration will be supervised online by a FAIRplus expert who will be able to consult any issue or questions during this phase. At the end, each pair will write up their observations and preliminary solutions which will be presented in phase four.</p>

                    
                    
                    <p><strong>The phase four</strong> is a face-to-face meeting attended by all fellows. During the meeting, the fellows will present their FAIRification project results to demonstrate their understanding of FAIR data management and their capability of acting as FAIR data experts. At the end of the day, participants will receive formal certificates.</p>

                  

