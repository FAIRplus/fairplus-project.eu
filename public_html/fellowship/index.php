                  <p>The FAIRplus Fellowship Programme is a comprehensive training programme in FAIR data management. <strong>The programme will run for a period of approximately 8 months, starting in April 2021</strong> and will guide its fellows through a series of modules combining online learning, 'training on the job' and short face-to-face meetings. After completing the programme, the fellows will have the confidence to lead, advise and initiate FAIR data processes in their respective companies and organisations.</p>
                
                  <h2>How to apply</h2>
                  <p>Most fellows will be selected from FAIRplus partner organizations (including EFPIA participants). Limited spaces are reserved for applicants from outside the FAIRplus consortium, in particular from small and medium-sized enterprises (SMEs).</p>

                

                  <p>Interested candidates working in academia or EFPIA companies affiliated with the FAIRplus consortium or SMEs will be able to apply by submitting a FAIRification project they intend to develop during the Programme. Academic fellows will receive a small compensation fund for the time they spend completing the programme. <p>
                    
