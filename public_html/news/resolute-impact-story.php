                  <h1>FAIR from the start: Implementing FAIR data practices in the RESOLUTE project</h1>

									<figure class="top-margin bottom-margin">
									                        <img src="https://fairplus-project.eu/images/logos/resolute-m.png" alt="RESOLUTE project logo" class="low-top-margin float-left right-margin" style="max-width: 45%;">
									                    </figure>



<p>The main focus of RESOLUTE are solute carriers (SLCs), a group of proteins that transport substances such as nutrients or metabolites, but also drugs, across biological membranes. </p>

<p>Even though many of the SLCs have been linked to human diseases, including obesity or type 2 diabetes, only a handful of SLC proteins have been used as drug targets. The aim of RESOLUTE is to boost research on SLCs and to establish them as an attractive target class for medical research and drug development.</p>



<h2>Make it findable</h2>

<p>The RESOLUTE project has been working with the FAIRplus experts throughout 2019 to FAIRify the baseline data generated early on in the project and to establish sustainable FAIR data processes for all future data produced by the consortium.</p>

<p><strong>Ulrich Goldmann</strong> (pictured on the left), data scientist at the Research Center for Molecular Medicine of the Austrian Academy of Sciences (CeMM) in Vienna, Austria, and data manager within the RESOLUTE project, explains:</p>

<p>'There are many levels of FAIR, some can be achieved relatively easily, others are much more difficult. At the beginning of the project, we asked what level of FAIR would bring the best return on investment. The major benefit of FAIR data that we saw right from the start is that it makes submission to public repositories a lot easier. So our first criterion was FAIR enough so that we can submit our data to domain-specific repositories.'
</p>

<figure class="top-margin">
                        <img src="https://fairplus-project.eu/images/goldmann-sedlyarov.jpg" alt="Ulrich Goldmann and Vitaly Sedlyarov" style="max-width: 55%;" class="low-top-margin right-margin float-right">
</figure>


<p>To make raw data submission-ready, they need to be properly annotated. This requires not only relevant scientific knowledge but also familiarity with the different metadata standards and ontologies. The FAIRification experts from the FAIRplus Squad teams worked with data managers from RESOLUTE to bring the baseline data to a submission-ready form.</p>


<p>'Transcriptomics data are already at a high level of FAIRness. But other data types are more difficult to FAIRify. Proteomics and metabolomics, for example, need a lot of work  you need to collect relevant metadata and apply ontologies for every datatype,' says <strong>Vitaly Sedlyarov</strong> (pictured on the right), Goldmann's colleague at CeMM in Vienna. </p>



<p>The collaboration with FAIRplus was crucial in helping the RESOLUTE researchers to submit their data to European resources, such as the European Nucleotide Archive, ProteomeXchange or Metabolights.</p>

<p>The FAIRplus Squad teams have summarised their experience from FAIRifying the RESOLUTE transcriptomic data in a <a href="https://fairplus.github.io/cookbook-dev/recipes/applied-examples/resolute_raw/rawRecipe_resolute.html">RESOLUTE FAIRification Recipe, published in FAIRplus's FAIR cookbook</a>. The recipe identifies the appropriate identifiers for the data and proposes a new schema for transcriptomic data, based on existing community standards and submission guidelines of popular transcriptomics databases.</p>

<p>Even though this was just the first step in the FAIRification process, it already has a big impact on the potential reuse of RESOLUTE outcomes. Ulrich Goldmann explains:</p>

<p>'RESOLUTE started in summer of 2018; so far we have the baseline expression data from parental cell lines. But it's essential that these data are readily available in a public database. All future results and data that we will generate have to be compared with the baseline data. Therefore the baseline data must be findable and accessible to be useful for our researchers. This is a very important and direct benefit of the FAIRification process.'</p>



<h2>FAIRification for the future </h2>

<p>In a data-intensive research project, a detailed data management plan can make the difference between success and failure. This is all the more important for RESOLUTE which aims to promote SLCs as attractive drug targets. Without the data being available to the wide scientific community, we cannot expect any meaningful impact in terms of new SLC research and new SLC based medicines.</p>

<p>'If we don't record metadata properly from the beginning, it might never happen. At a later stage, it would be almost impossible or at least much harder to do any kind of FAIRification,' says Vitaly Sedlyarov. </p>

<p>'Working together with FAIRplus prompted us to think about many aspects of FAIR data principles. Having the right ontologies and proper identifiers helped us to build an internal database for the entire consortium. We believe that RESOLUTE could make a good use case for future IMI projects, as all these projects must figure out their own way of organising  data. Our experience should help other IMI researchers to build such FAIR repositories for their own projects.'</p>

<p>'Thanks to FAIRplus we now have an extensive data management plan and computational solutions available. It makes a good starting point. We are still at the beginning of our project, but it's already clear that FAIR data make all the difference in building a good repository and will inevitably raise the quality and reusability of our data.'</p>

<h2>About RESOLUTE</h2>

<p>RESOLUTE is a public-private partnership funded by the Innovative Medicines Initiative 2 Joint Undertaking under Grant Agreement No 777372. Led by Research Center for Molecular Medicine of the Austrian Academy of Sciences and Pfizer Ltd., it has 13 partners from academia and industry. </p>

