                    <h1>FAIRplus project outcomes presented in the 3rd FAIRplus Innovation and SME Forum</h1>

                    <h3>The third FAIRplus Innovation and SME Forum was held on 17 May 2022 to present the project's outcomes and foster the development of an innovation ecosystem based on the FAIR principles. Over 200 scientists, data managers and IT experts from academia, Small-Medium Enterprises (SMEs) and industry gathered online to discuss the challenges and opportunities in implementing FAIR data principles in life science research.
                    </h3>

                    <figure class="top-margin">
                        <img src="/images/FAIRplus-SME-event-intro-slide.png" alt="FAIRplus SME event intro and break slide" style="max-width: 75%;" class="low-top-margin right-margin">
