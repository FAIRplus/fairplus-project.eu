                    <h2>Progress of FAIRplus and FAIR cookbook</h2>
                    <p>Starting in 2019, the FAIRplus project aims to develop tools and guidelines for making life science data FAIR (Findable, Accessible, Interoperable, Reusable). The first session of the forum focused on the progress of the project and FAIR Cookbook, an extensive online resource providing tools and procedures to make and keep data FAIR:
                    </p>
                    <ul>
                        <li>Philippe Rocca-Serra from the University of Oxford’s e-Research Centre presented an overview of the FAIR cookbook, an open, comprehensive resource with ‘recipes’ for making different types of life science data FAIR.
                        </li>
                        <li>Ibrahim Emam from Imperial College London talked about how to apply the Dataset Maturity Model (FAIR-DMM), a tool to develop, assess and refine a strategic FAIR data transformation program for organisations.
                        </li>
                        <li>Wei Gu from the University of Luxembourg and Vassilios Ioannidis from Swiss Institute of Bioinformatics shared their experience of applying ‘recipes’ from the FAIR Cookbook.
                        </li>
                    </ul>

                    <p>The FAIRplus Fellowship Programme that started in May 2021 is an important and valuable outcome of the FAIRplus project. During the forum, Andreas Pippow from Fraunhofer Institute shared how to build FAIR capacity through the FAIRplus Fellowship Programme, a combination of comprehensive FAIR data training and “learning by doing”. . FAIRplus Fellows from different organisations also shared their experiences on implementing FAIRfication in their organisations and the importance of it. One of the fellows, Irina Balaur, from the University of Luxembourg stressed that “FAIRification is a process, not a single milestone.” At the end of the first session, Areti Manola from Janssen shared the motivation and benefits of making data FAIR from the industry perspective.
                    </p>
                    <h2>Strategic partnerships, sustainability and experience built through IHI projects</h2>
                    <p>One of the FAIRplus project’s goals is to establish a process for selecting and prioritising IHI project databases for FAIRification. The second session of the forum focused on strategic partnerships, sustainability and experience built through Innovative Health Initiative (IHI) projects:
                    </p>
                    <ul>
                        <li>Colm Carroll from Innovative Health Initiative gave a keynote speech on IHI’s FAIR experience, mentioning the importance of “implementing FAIR into future IHI projects”.
                        </li>
                        <li>Thomas Liener from Pistoia Alliance shared strategic partnerships under FAIR principles.
                        </li>
                        <li>Nick Lynch from OpenPHACTS and Susanna Sansone from the University of Oxford presented the sustainability of FAIR principles.
                        </li>
                    </ul>
                    <p>
                        Nick Lynch mentioned that FAIR sustainability requires “alignment with organisation’s goals, leadership, and embedding in the digital strategy among others.” Susanna Sansone addressed that, “for the sustainability of the FAIR Cookbook, we need to work on four fronts, naming Content, Infrastructure, Embedding, and Endorsements.”
                    </p>
                    <h2>Discussion on a FAIRer future</h2>
                    <p>The last session of the forum was a panel discussion focusing on the future of FAIR implementation. Bérénice Wulbrecht from Ontoforce, Carole Goble from the University of Manchester, Colin Wood from AstraZeneca, and Kostas Repanas from the European Commission discussed the current practices, costs and benefits of FAIR implementation in research and development.</p>

                    <p>Throughout the forum, one of the critical topics discussed the most was the sustainability of the project’s accomplishments. In the General Assembly over the following two days, the project team also discussed and made plans on how to continue to disseminate the work of FAIRplus in the future.</p>
