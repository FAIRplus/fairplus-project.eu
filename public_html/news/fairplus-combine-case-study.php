                    <p>The <a href="https://www.imi.europa.eu/projects-results/project-factsheets/combine" target="_blank">IMI COMBINE project</a> is part of <a href="https://www.imi.europa.eu/projects-results/project-factsheets/amr-accelerator" target="_blank">IMI’s Antimicrobial Resistance (AMR) Accelerator programme</a>. COMBINE is a centralised hub that supports the AMR community and is important for the scientific capacity building of AMR Accelerator projects. The project’s aim is to improve the animal models used in AMR research and develop improved tools to reliably translate results in animals into results in humans.</p>


                    <p>To be able to combat pathogenic drug-resistant microbes, we need better use of existing data to stimulate the development of new antimicrobial tools and treatments. However, the majority of the current AMR data is hardly reusable due to a lack of interoperability.</p>

