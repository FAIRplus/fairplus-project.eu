                  <h1 class="low-bottom-margin">FAIR Cookbook incorporated into the IMI guidance and tools catalogue</h1>
                    <p><i>Since the FAIRplus project started in 2019, partners have been developing one of the main outputs, the FAIR Cookbook. In December 2021, the resource was incorporated into the Innovative Medicine Initiative (IMI) tools catalogue and its guidance for open access to publications and research data.</i></p>
                    <p>Including the resource in the <a href="https://www.imi.europa.eu/sites/default/files/uploads/documents/resources-for-projects/IMI2_OpenAcesGuidelines_Updated2021.pdf" target="_blank">IMI guidance</a> and the <a href="https://www.imi.europa.eu/projects-results/catalogue-project-tools" target="_blank">tools catalogue</a> sets a precedent for further uptake. It positions the <a href="https://w3id.org/faircookbook" target="_blank">FAIR Cookbook</a> as a recommended resource for making life science data FAIR (Findable, Accessible, Interoperable and Reusable). This is an indispensable step for unlocking hidden data insights that could, for example, impact patients’ health and help discover new drugs.
                    </p>
                    <p><i>‘We are delighted to include the FAIR Cookbook recipes in the new release of our data management guidelines. The FAIR Cookbook provides practical guidance to FAIRify research data and is a big step towards unlocking its full value and delivering better treatments for patients’, </i>says Colm Carroll, Scientific Project Manager at IMI.</p>
                    <p>Authored by industry and academic data management professionals, including ELIXIR members, the FAIR Cookbook presents a step-by-step open-source guide for making life sciences data FAIR. It provides researchers and data stewards with recipes to master FAIR data beyond generic principles. Real-life examples also offer policymakers and trainers the ingredients to produce recommendations and educational materials.
                    </p>
                    <p>Users can already find applied examples from IMI projects such as:</p>
                    <ul class="low-padding">
                        <li><a href="http://w3id.org/faircookbook/FCB042" target="_blank">eTOX – toxicity datasets</a></li>
                        <li><a href="http://w3id.org/faircookbook/FCB043" target="_blank">ND4BB – chemical activities datasets</a></li>
                        <li><a href="http://w3id.org/faircookbook/FCB044" target="_blank">Oncotrack – clinical cohort datasets</a> </li>
                        <li><a href="http://w3id.org/faircookbook/FCB045" target="_blank">ReSOLUTE – transcriptomics datasets</a> </li>
                    </ul>

                    <p>The Cookbook is an evergreen resource. Allowing endless contributions from any expert that wishes to participate, the resource will continually reshape its contents to tackle new user needs.</p>
                    <p>If you want to participate, join and <a href="https://fairplus.github.io/the-fair-cookbook/content/recipes/help.html#fcb-help" target="_blank">learn how to contribute</a> or contact us at
